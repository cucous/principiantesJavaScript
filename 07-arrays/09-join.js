let arr1 = ['Iván', 'Chanchito', 'Arthur'];
let mensaje = arr1.join(', ');
console.log(mensaje); // Iván, Chanchito, Arthur

let saludo = "Hola mundo! desde JavaScript";
let dividio = saludo.split(" ");
console.log(dividio); // ['Hola', 'mundo!', 'desde', 'JavaScript']
console.log(dividio.join("-")); // Hola-mundo!-desde-JavaScript